#include <stdio.h>             //  ต้องใส่ # และ <> ครอบไลบรารี stdio.h  ขาด # และ < >

int main() {                   //  main() คือฟังก์ชันหลักของโปรแกรม
    int a = 10;                //  ต้องมี ; ปิดท้ายบรรทัด (ประกาศตัวแปรชนิด int)     ขาด ; ปิดท้าย
    printf("a = %d", a);       //  ใช้ printf แสดงค่าของตัวแปร a ต้องมี ; ปิดท้าย    ขาด ; ปิดท้าย
    return 0;                  //  คืนค่า 0 เป็นการจบโปรแกรมอย่างถูกต้อง
}